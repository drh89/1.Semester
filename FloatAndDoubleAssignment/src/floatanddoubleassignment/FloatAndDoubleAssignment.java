package floatanddoubleassignment;

/**
 *
 * @author Dennis
 */
public class FloatAndDoubleAssignment {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        double myPounds = 200d;
        
        double myKilos = 0.45359237d;
        
        double myFinal = myPounds * myKilos;
        
        System.out.println("myFinal = " + myFinal);
        
        
    }
    
}
