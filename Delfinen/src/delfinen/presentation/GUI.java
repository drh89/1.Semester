    private void jButtonShowTop5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonShowTop5ActionPerformed
        if (jComboBoxTop5Disciplines.getSelectedIndex() == 0) {
            jTextAreaSwimmerOverview.setText(c.getTop5(Discipline.CRAWL));
            if (c.getTop5(Discipline.CRAWL).isEmpty()) {
                jTextAreaSwimmerOverview.setText("No results yet");
            }

        }
        if (jComboBoxTop5Disciplines.getSelectedIndex() == 1) {
            jTextAreaSwimmerOverview.setText(c.getTop5(Discipline.BACKSTROKE));
        }
        if (jComboBoxTop5Disciplines.getSelectedIndex() == 2) {
            jTextAreaSwimmerOverview.setText(c.getTop5(Discipline.BREASTSTROKE));
        }
        if (jComboBoxTop5Disciplines.getSelectedIndex() == 3) {
            jTextAreaSwimmerOverview.setText(c.getTop5(Discipline.BUTTERFLY));
        }
    }//GEN-LAST:event_jButtonShowTop5ActionPerformed

