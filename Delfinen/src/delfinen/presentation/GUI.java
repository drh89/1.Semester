    private void jButtonGetSwimmerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGetSwimmerActionPerformed
        for (CasualSwimmer swimmer : c.swimmersFromFile()) {
            if (swimmer.getId() == Integer.parseInt(jTextFieldID.getText())) {
                jTextFieldAge.setText(Integer.toString(swimmer.getAge()));
                jTextFieldName.setText(swimmer.getName());

                if (swimmer.getStatus().toString().equalsIgnoreCase("Active")) {
                    jComboBoxStatus.setSelectedIndex(0);
                }
                if (swimmer.getStatus().toString().equalsIgnoreCase("Passive")) {
                    jComboBoxStatus.setSelectedIndex(1);
                }

                if (swimmer.getTeam().equalsIgnoreCase("Casual")) {
                    jComboBoxTeam.setSelectedIndex(0);
                }
                if (swimmer.getTeam().equalsIgnoreCase("Junior")) {
                    jComboBoxTeam.setSelectedIndex(1);
                }
                if (swimmer.getTeam().equalsIgnoreCase("Senior")) {
                    jComboBoxTeam.setSelectedIndex(2);
                }

                if (swimmer.isPaid()) {
                    jComboBoxPaid.setSelectedIndex(0);
                }
                if (!swimmer.isPaid()) {
                    jComboBoxPaid.setSelectedIndex(1);
                }
            }
        }
    }//GEN-LAST:event_jButtonGetSwimmerActionPerformed

    private void jButtonEditSwimmerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditSwimmerActionPerformed
        boolean paid = false;
        int id = Integer.parseInt(jTextFieldID.getText());
        int age = Integer.parseInt(jTextFieldAge.getText());
        String name = jTextFieldName.getText();
        String status = (String) this.jComboBoxStatus.getSelectedItem();
        String upperStatus = status.toUpperCase();
        String team = (String) this.jComboBoxTeam.getSelectedItem();
        if (jComboBoxPaid.getSelectedItem().equals("Yes")) {
            paid = true;
        }
        if (jComboBoxPaid.getSelectedItem().equals("No")) {
            paid = false;
        }

        c.editSwimmer(id, age, name, Status.valueOf(upperStatus), team, paid);

        jTextFieldID.setText("");
        jTextFieldName.setText("");
        jTextFieldAge.setText("");

    }//GEN-LAST:event_jButtonEditSwimmerActionPerformed

    private void jButtonAddSwimmerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddSwimmerActionPerformed
        ArrayList<Discipline> disciplines = new ArrayList<>();
        CompetitionSwimmer compSwimmer;
        boolean paid = false;
        int age = Integer.parseInt(jTextFieldAge.getText());
        String name = jTextFieldName.getText();
        String status = (String) this.jComboBoxStatus.getSelectedItem();
        String upperStatus = status.toUpperCase();
        String team = (String) this.jComboBoxTeam.getSelectedItem();
        if (jComboBoxPaid.getSelectedItem().equals("Yes")) {
            paid = true;
        }
        if (jComboBoxPaid.getSelectedItem().equals("No")) {
            paid = false;
        }
        if (this.jComboBoxTeam.getSelectedIndex() == 0) {
            c.newCasualSwimmer(age, name, Status.valueOf(upperStatus), team, paid);
        } else {
            compSwimmer = c.newCompSwimmer(age, name, Status.valueOf(upperStatus), team, paid);
            if (jRadioButtonBreast.isSelected()) {
                disciplines.add(Discipline.BREASTSTROKE);
            }
            if (jRadioBack.isSelected()) {
                disciplines.add(Discipline.BACKSTROKE);
            }
            if (jRadioButtonCrawl.isSelected()) {
                disciplines.add(Discipline.CRAWL);
            }
            if (jRadioButtonButter.isSelected()) {
                disciplines.add(Discipline.BUTTERFLY);
            }
            c.addDisciplines(disciplines, compSwimmer);

    }//GEN-LAST:event_jButtonAddSwimmerActionPerformed
        jTextFieldName.setText("");
        jTextFieldAge.setText("");
    }

